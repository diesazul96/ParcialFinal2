package vo;

public class ColmenitaVO {
    private int id;
    private int poblacion;
    private String calidad;
    private boolean reina;
    private boolean miel;
    private int pCera;
    private int pAlimento;
    private int pCria;
    private int pVacios;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPoblacion() {
        return poblacion;
    }

    public void setPoblacion(int poblacion) {
        this.poblacion = poblacion;
    }

    public String getCalidad() {
        return calidad;
    }

    public void setCalidad(String calidad) {
        this.calidad = calidad;
    }

    public boolean isReina() {
        return reina;
    }

    public void setReina(boolean reina) {
        this.reina = reina;
    }

    public boolean isMiel() {
        return miel;
    }

    public void setMiel(boolean miel) {
        this.miel = miel;
    }

    public int getpCera() {
        return pCera;
    }

    public void setpCera(int pCera) {
        this.pCera = pCera;
    }

    public int getpAlimento() {
        return pAlimento;
    }

    public void setpAlimento(int pAlimento) {
        this.pAlimento = pAlimento;
    }

    public int getpCria() {
        return pCria;
    }

    public void setpCria(int pCria) {
        this.pCria = pCria;
    }

    public int getpVacios() {
        return pVacios;
    }

    public void setpVacios(int pVacios) {
        this.pVacios = pVacios;
    }
    
        
    
}
